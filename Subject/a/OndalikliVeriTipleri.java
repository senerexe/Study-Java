package a;

public class OndalikliVeriTipleri {

	public static void main(String[] args) {
		
		float sayi1 = 34.5f;
		float sayi2 = 34.5F;
		float sayi3 = 34;
		
		System.out.println(sayi1);
		System.out.println(sayi2);
		System.out.println(sayi3);
		
		double sayi4 = 3;
		double sayi5 = 3.14;
		
		System.out.println(sayi4);
		System.out.println(sayi5);

	}

}
