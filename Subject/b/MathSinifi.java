package b;

public class MathSinifi {

	public static void main(String[] args) {
		// Math sınıfı metotları
		// Math.ceil(x) => bir büyük tam sayıya yuvarlama yapar.
		// Math.cos(x) => cos değerini döner
		// Math.sin(x) => sin değerini döner
		// Math.floor(x) => küçük tam sayıya yuvarlar
		// Math.max(x,y) => büyük sayıyı verir
		// Math.min(x,y) => küçük sayıyı verir
		// Math.pow(3,5) => 3^5 üs alır
		// Math.random() => 1 ve 0 arasında rastegele değer
		// Math.round(x) => yakın olan tam sayıya döner
		// Math.abs(x) => mutlak değer
		// Math.sqrt(x) => karekök alma
		
		System.out.println(Math.ceil(1.5));
		System.out.println(Math.sin(60));
		System.out.println(Math.floor(1.5));
		System.out.println(Math.max(5,6));
		System.out.println(Math.pow(3,5));
		System.out.println(Math.random());
		System.out.println(Math.round(1.5));
		System.out.println(Math.abs(-4));
		System.out.println(Math.sqrt(25));
		
		
		
		
		
		
		
		
	}

}
